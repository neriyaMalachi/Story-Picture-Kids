
/**
 * Contains pre-defined demo stories for different themes
 */

/**
 * Creates a space-themed story with the child's name
 * Each paragraph corresponds to a specific space-themed image
 */
export const createSpaceStory = (name: string): string => {
  return `# ${name} והרפתקה בחלל

בוקר אחד, ${name} התעורר וגילה משהו מדהים – חללית קטנה נחתה בחצר ביתו!
${name} לבש מיד את חליפת החלל המיוחדת, שם קסדה על ראשו, ועמד נרגש מוכן להרפתקה.
"אני אסטרונאוט אמיתי עכשיו!" קרא ${name} בהתרגשות, מנופף בידו לשלום לכדור הארץ.
בלב פועם מהתרגשות, ${name} ידע שהרפתקה גדולה עומדת להתחיל.

החללית המריאה במהירות האור והעלתה את ${name} גבוה אל השמיים.
${name} ישב בתוך החללית האדומה המהירה, מביט בחלון איך כדור הארץ הופך קטן מאחוריו.
"וואו! אני טס במהירות כה גבוהה!" צחק ${name} כשהחללית חלפה בין כוכבים נוצצים.
הלהבות מאחורי החללית האירו את החלל בצבעי כתום ואדום מרהיבים.

אחרי מסע מרתק, ${name} הגיע לגלקסיה חדשה ומסתורית.
${name} יצא מהחללית, הניף דגל והכריז: "אני ${name}, חוקר החלל האמיץ מכדור הארץ!"
הוא הרגיש כמו גיבור אמיתי, עומד גאה על פני כוכב לכת שאף אדם לא ביקר בו קודם לכן.
${name} הסתכל סביבו בהתפעלות - כוכבים זוהרים מילאו את השמיים השחורים מעליו.

${name} רכב על אופנוע חלל מיוחד כדי לחקור את הכוכב החדש.
"זהו כוכב מדהים עם אבנים ורודות ואדמה כחולה!" קרא ${name} בהתרגשות.
האופנוע החללי טס מעל פני השטח המוזרים של הכוכב, מרחף מעל תצורות סלע מוזרות.
${name} ראה הרים גבוהים, מכתשים עמוקים וימים מבעבעים בצבעים שלא ראה מעולם.

לקראת ערב, ${name} ישב על פני הכוכב להפסקה קצרה ולארוחה קלה.
${name} נח על אבן גדולה, מתבונן בשקיעה המיוחדת - שלושה שמשים שוקעים בו-זמנית!
"זה המקום הכי מדהים שראיתי בחיי," חשב ${name}, נהנה מהרגע השקט והקסום.
האור הרך האיר את פניו של ${name} בצבעי זהב ורוד, והוא חייך חיוך רחב מאושר.

לפני שחזר הביתה, ${name} מצא מתנה מיוחדת - כוכב זוהר קטן שנפל מהשמיים.
${name} החזיק בידו את הכוכב הקסום, שהאיר באור כחול-סגול מרהיב.
"אקח את הכוכב הזה הביתה כמזכרת מהמסע המופלא שלי," אמר ${name} לעצמו.
הכוכב הקטן זהר והבהב, כאילו שמח להצטרף ל${name} בחזרה לכדור הארץ.

**סוף**`;
};

/**
 * Creates an ocean-themed story with the child's name
 * Each paragraph corresponds to a specific ocean-themed image
 */
export const createOceanStory = (name: string): string => {
  return `# ${name} והרפתקה בים

יום אחד, ${name} הלך לחוף הים עם משפחתו לטיול מהנה.
השמש זרחה במלוא עוצמתה והמים היו כחולים וצלולים להפליא.
${name} שחה במים הרדודים, מוקף בחברים, צוחק ומתיז מים בשמחה רבה.
רוח קלילה העבירה את ריח המלח באוויר, ו${name} נשם עמוק את ניחוח הים.

לפתע, ${name} ראה משהו מנצנץ מתחת למים העמוקים יותר.
${name} לקח נשימה עמוקה וצלל מתחת לפני המים, סקרן לגלות מה נמצא שם.
עם משקפת צלילה על פניו, ${name} שחה עמוק יותר, מוקף בבועות כסופות.
עולם חדש ומופלא נפתח לפניו - שוניות אלמוגים צבעוניות ודגים מרהיבים.

${name} המשיך לשחות בשמחה, מגלה את פלאי האוקיינוס.
דגים קטנים בצבעי כתום, צהוב וכחול שחו סביבו בסקרנות, כאילו קיבלו את פניו.
${name} התמוגג מהתחושה של השחייה החופשית במים הצלולים, מוקף ביופי הטבע.
ידיו חתרו בעדינות במים, ורגליו הניעו אותו קדימה בתנועות קלילות.

אחרי הצלילה, ${name} חזר לחוף לשחק בחול ובמים הרדודים.
${name} בנה טירות חול מפוארות, קפץ בגלים הקטנים ורץ לאורך קו המים.
הוא מצא צדפים יפים וגרגרי חול נוצצים שזהרו בשמש כמו אבני חן קטנות.
שמחה מילאה את ליבו של ${name} בזמן שהוא נהנה מכל רגע על החוף הקסום.

חברים חדשים הצטרפו ל${name}, וביחד הם שיחקו במשחקי חוף כיפיים.
${name} וחבריו התיזו מים, זרקו כדור ים צבעוני וצחקו ללא הפסקה.
הם חפרו בור גדול בחול ויצרו תעלות מים שהובילו אל הים, כמו מהנדסים קטנים.
"זה היום הכי כיף בחיים שלי!" קרא ${name} כשקפץ לתוך המים עם חבריו.

לקראת סוף היום, ${name} החליט לבנות טירת חול מיוחדת ומרשימה.
${name} אסף דליים של חול רטוב, צדפים צבעוניים ומקלות קטנים לקישוט.
בריכוז רב, הוא בנה מגדלים, חומות, וחפר תעלת מים מסביב לטירה המפוארת.
"זוהי הטירה של מלך הים," הכריז ${name} בגאווה, מצביע על יצירת המופת שלו.

**סוף**`;
};

/**
 * Creates an adventure-themed story with the child's name
 * Each paragraph corresponds to a specific adventure-themed image
 */
export const createAdventureStory = (name: string): string => {
  return `# הרפתקה מופלאה של ${name}

זה היה יום מושלם להרפתקה, ו${name} היה מוכן לצאת למסע מרגש.
עם כובע מטיילים על ראשו ותרמיל על גבו, ${name} יצא אל השביל שהוביל ליער.
השמש זרחה, ציפורים צייצו בעליזות, והטבע קרא ל${name} לבוא ולגלות את סודותיו.
${name} צעד בביטחון, מחייך חיוך רחב, נרגש לקראת ההרפתקאות שמצפות לו.

${name} טיפס בשבילים, עם תרמיל הגב שלו מלא בכל הציוד הדרוש למסע.
הוא הלך דרך יערות עבותים, חצה נחלים קטנים, וטיפס על גבעות נמוכות.
"אני מטייל אמיתי עכשיו," חשב ${name} לעצמו בגאווה, אוחז במפה שלו.
בכל צעד, ${name} גילה צמחים חדשים, עקבות של חיות, וסימנים מסתוריים בטבע.

המסע הוביל את ${name} למקום מיוחד – מערה מסתורית שהייתה חבויה בין העצים.
${name} עטה את כובע החוקרים שלו, הדליק את הפנס, ונכנס בזהירות אל המערה.
בתוך המערה, ${name} גילה ציורי קיר עתיקים וסימנים מוזרים שנראו כמו מפה.
"זה חייב להוביל לאוצר!" קרא ${name} בהתרגשות, מעתיק את הסימנים לפנקס שלו.

כשהגיע הערב, ${name} הקים מחנה קטן בקרחת יער מושלמת.
הוא פרש את האוהל הקטן שלו, הדליק מדורה קטנה, והתכונן ללילה בטבע.
${name} ישב ליד המדורה, נהנה מחום האש הנעים ומהכוכבים שהחלו להופיע בשמיים.
זה היה רגע קסום – להיות לבד בטבע, להקשיב לקולות היער, ולהרגיש חלק מהעולם הגדול.

בבוקר שלמחרת, ${name} פגש חברים חדשים שהיו גם הם במסע טיולים משלהם.
הם החליטו לחבור יחד להמשך המסע, שיתפו סיפורים וחוויות מהטיולים שלהם.
${name} הרגיש שמחה גדולה להיות חלק מקבוצת המטיילים הקטנה והמגובשת.
יחד הם טיפסו על עצים, קפצו מעל שלוליות, ועזרו זה לזה בדרכים הקשות.

הקבוצה הגיעה לגן בוטני מיוחד, מלא בצמחים נדירים ופרחים מרהיבים.
${name} וחבריו טיילו בין השבילים המטופחים, מתפעלים מהצבעים והריחות.
הם ראו פרפרים צבעוניים, ציפורי קוליברי זעירות, וחיפושיות מבריקות.
"איזה עולם מופלא!" אמר ${name}, כשהביט סביבו בהתפעלות על פלאי הטבע.

**סוף**`;
};

/**
 * Returns a story based on the specified theme
 */
export const getStoryByTheme = (name: string, theme: string): string => {
  switch (theme) {
    case 'space':
      return createSpaceStory(name);
    case 'ocean':
      return createOceanStory(name);
    default:
      return createAdventureStory(name);
  }
};
